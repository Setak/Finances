public class Accounts {

    private float checkingAcct;
    private float savingsAcct;


    public Float getCheckingAcct() {
        return checkingAcct;
    }

    public Float getSavingsAcct() {
        return savingsAcct;
    }

    public void setCheckingAcct(float newCheckingAcct) {
        checkingAcct = newCheckingAcct;
    }

    public void setSavingsAcct(float newSavingsAcct) {
        savingsAcct = newSavingsAcct;
    }

}
